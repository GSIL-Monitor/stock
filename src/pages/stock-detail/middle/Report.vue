<template>
<div
    class="info_wrap"
>
    <div
        class="info_vessel"
        v-show="dataStore.length"
        v-infinite-scroll="loadMore"
        infinite-scroll-disabled="busy"
        infinite-scroll-distance="60"
        infinite-scroll-throttle-delay="100"
        ref="scrollContainer"
    >
        <table
            v-if="dataStore.length"
            class="info_vessel_table"
            @click="handleClick"
        >
            <tbody>
                <tr
                    v-for="(item, index) of dataStore"
                    :key="index"
                    class="info_tr"
                >
                    <td
                        class="info_ellipsis_td"
                    >
                        <span
                            class="td-openContent"
                            :data-index="index"
                            :title="item.report_title"
                        >
                            {{item.report_title || '--'}}
                        </span>
                    </td>
                    <td
                        class="info_ico_td"
                    >
                        <span
                            :class="['stockDetial-read-pdf', formatIco(item.file_type).imgClass]"
                        >
                        </span>
                    </td>
                    <td
                        class="info_org_td"
                    >
                        {{item.organ_name || '--'}}
                    </td>
                    <td
                        class="info_date_td"
                    >
                        {{formatDate(item.create_date)}}
                    </td>
                </tr>
            </tbody>
        </table>
    </div>
    <div
        v-if="noData"
        class="extend_nodata"
    >{{noDataMsg}}</div>
</div>
</template>

<script>
import {
    mapState,
    mapGetters,
} from 'vuex'
import {
    getResearchReport,
} from '@service/'
import formatInfoDate from '@formatter/information/date'
import fileType from '@formatter/information/fileType'
import {
    openReport,
} from './open-information'
import {
    getReportLine,
} from '@c/utils/util'

import informationBusyMixin from '../mixins/information-busy-mixin'

export default {
    name: 'Report',
    mixins: [
        informationBusyMixin,
    ],
    created() {
        this.fetchData()
    },
    data() {
        return {
            ROWS: 10,
            page: 1,
            dataStore: [],
            noData: false,
            noDataMsg: '暂无相关研报',
            filterFields: [
                'create_date',
                'file_type',
                'guid',
                'read_mark',
                'report_title',
                'organ_name',
                'organ_id',
                'has_read',
                'count',
            ],
        }
    },
    computed: {
        ...mapGetters([
            'isAStock',
        ]),
        ...mapState([
            'stock_code',
            'full_code',
        ]),
        stockParam() {
            return {
                code: this.stock_code,
                rows: this.ROWS,
                page: this.page,
                has_read: 1,
                fields: this.filterFields.join(';'),
            }
        },
        getParams() {
            if (this.isAStock) {
                return this.stockParam
            }
        },
        getApi() {
            if (this.isAStock) {
                return getResearchReport
            }
        },
    },
    methods: {
        fetchData() {
            let param = this.getParams
            let api = this.getApi
            const params = {
                options: param,
                callback0: data => {
                    this.dataStore = this.dataStore.concat(data)
                    this.setBusyState(data.length)
                },
                callback1001: () => {
                    this.busy = true
                    if (Object.is(this.dataStore.length, 0)) {
                        this.noData = true
                    }
                },
            }
            if (api) {
                api(params)
            }
        },
        formatDate(date) {
            return formatInfoDate(date)
        },
        formatIco(type) {
            return fileType(type)
        },
        openContent(index, data) {
            index = index + 1
            let title = data.stock_name ? `${this.titleName}——${data.stock_name}(${this.stock_code})` : this.titleName
            const param = {
                position: index,
                contentId: data.guid,
                params: {
                    rows: 6,
                    code: this.stock_code,
                    product_line: getReportLine(),
                    page: Math.ceil(index / 6)
                },
            }
            openReport(param, title)
        },
        handleClick(event) {
            const target = event.target

            if (target.className.includes('td-openContent')) {
                // 原生弹框
                const index = Number(target.dataset.index)
                const targetData = this.dataStore[index]
                this.openContent(index, targetData)
            } else if (target.className.includes('info_ico_td')) {
                // TODO:原文

            }
        },
    },
    watch: {
        full_code() {
            this.resetState()
            this.fetchData()
        },
    },
}
</script>

